#include<unistd.h>

int	main(int ac, char **av)
{
	int	c;
	int	i;

	c = 1;
	i = 0;
	if (ac > 1)
	{
		while (av[c])
		{
			while (av[c][i])
			{
				write(1, &av[c][i], 1);
				i++;
			}
			write(1, "\n", 1);
			c++;
			i = 0;
		}
	}
	return (0);
}
