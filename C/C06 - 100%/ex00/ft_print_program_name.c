#include<unistd.h>

int	main(int ac, char **av)
{
	int	c;

	if (ac > 0)
	{
		c = 0;
		while (av[0][c])
		{
			write(1, &av[0][c], 1);
			c++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
