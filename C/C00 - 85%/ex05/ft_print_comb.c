#include<unistd.h>
#include<stdio.h>

void	ft_putchar(char e)
{
	write (1, &e, 1);
}

void	ft_print(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7' || b != '8' || c != '9')
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	b = '1';
	c = '2';
	while (a < '8')
	{		
		while (b <= '8')
		{		
			while (c <= '9')
			{	
				ft_print(a, b, c);
				c++;
			}
			b++;
			 c = b + 1;
		}
		a++;
		b = a;
	}
}
